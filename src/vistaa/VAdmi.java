    private void jcbDiasAsigHItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbDiasAsigHItemStateChanged

        if(evt.getStateChange()==ItemEvent.SELECTED){
            HorarioDiaEspecialidad hor=(HorarioDiaEspecialidad)jcbDiasAsigH.getSelectedItem();
      
       HorarioDiaEspecialidad horBus=daoH.lisHorariosOnly(hor.getId());
       
       if(hor.getId().equals("0")){
           jtxHoraInAsigH.setText("");
           jtxHoraFinAsigH.setText("");
           btnActHorarioMedAsigH.setEnabled(false);
       }else{
           jtxHoraInAsigH.setText(horBus.getHinicio());
           jtxHoraFinAsigH.setText(horBus.getHfin());
           btnActHorarioMedAsigH.setEnabled(true);
       }
            
        }


    }//GEN-LAST:event_jcbDiasAsigHItemStateChanged

